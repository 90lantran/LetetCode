package letcode;

import java.util.Comparator;

public class LocationTime {

	String location;
	int time;

	public LocationTime() {

	}

	public LocationTime(String location, int time) {
		this.location = location;
		this.time = time;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

}
